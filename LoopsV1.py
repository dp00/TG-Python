# Loops

for mynum in [1, 2, 3, 4, 5]:
    print("Hello ", mynum)


food=["ice cream","pizza","strawberries","chocolate", "lasagna"]
for x in range(0,len(food)):
  print(x, food[x])


food=["ice cream","pizza","strawberries","chocolate", "lasagna"]
index=0
